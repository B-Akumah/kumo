package edu.csis.model;

/**
 * The AccountType enum represents the type of a bank account, indicating whether it is a savings account or a checking account.
 *
 * @author bakumah
 */
public enum AccountType {
    /**
     * Indicates that the bank account is a savings account.
     * Savings accounts are typically used to save money and may earn interest.
     */
    SAVINGS,

    /**
     * Indicates that the bank account is a checking account.
     * Checking accounts are used for daily transactions and may have limited or no interest earnings.
     */
    CHECKING
}
